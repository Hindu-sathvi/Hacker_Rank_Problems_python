#  1. STRING n
#  2. INTEGER k
#

def superDigit(n, k):
    # Write your code here
     initial_sum = sum(int(digit) for digit in n)
     total_sum = initial_sum * k
     def recursive_super_digit(x):
         if x < 10: 
             return x
         return recursive_super_digit(sum(int(d) for d in str(x)))
     return recursive_super_digit(total_sum)
    
    
    

